package modelo;

public class Area {
	private String floresta;
	private String pantano;
	private String nova_area;
	private String nova_area_nome;
	
	public String getFloresta() {
		return floresta;
	}
	public void setFloresta(String floresta) {
		this.floresta = floresta;
	}
	public String getPantano() {
		return pantano;
	}
	public void setPantano(String pantano) {
		this.pantano = pantano;
	}
	public String getNova_area() {
		return nova_area;
	}
	public void setNova_area(String nova_area) {
		this.nova_area = nova_area;
	}
	public String getNova_area_nome() {
		return nova_area_nome;
	}
	public void setNova_area_nome(String nova_area_nome) {
		this.nova_area_nome = nova_area_nome;
	}

}
