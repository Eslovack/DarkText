package codigo;

public class Inimigo {

	private String nome;
	private String area;
	private Double force;
	private Double vida;
	private Double vidaMax;
	private Double manaMax;
	private Double mana;
	private Double velociadeMax;
	private Double velocidade;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public Double getForce() {
		return force;
	}
	public void setForce(Double force) {
		this.force = force;
	}
	public Double getVida() {
		return vida;
	}
	public void setVida(Double vida) {
		this.vida = vida;
	}
	public Double getVidaMax() {
		return vidaMax;
	}
	public void setVidaMax(Double vidaMax) {
		this.vidaMax = vidaMax;
	}
	public Double getManaMax() {
		return manaMax;
	}
	public void setManaMax(Double manaMax) {
		this.manaMax = manaMax;
	}
	public Double getMana() {
		return mana;
	}
	public void setMana(Double mana) {
		this.mana = mana;
	}
	public Double getVelociadeMax() {
		return velociadeMax;
	}
	public void setVelociadeMax(Double velociadeMax) {
		this.velociadeMax = velociadeMax;
	}
	public Double getVelocidade() {
		return velocidade;
	}
	public void setVelocidade(Double velocidade) {
		this.velocidade = velocidade;
	}
	
	

}

