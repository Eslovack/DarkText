package codigo;

public class Inimigo {

	private String nome;
	private String area;
	private Float force;
	private Float vida;
	private Float vidaMax;
	private Float manaMax;
	private Float mana;
	private Float velociadeMax;
	private Float velocidade;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public Float getForce() {
		return force;
	}
	public void setForce(Float force) {
		this.force = force;
	}
	public Float getVida() {
		return vida;
	}
	public void setVida(Float vida) {
		this.vida = vida;
	}
	public Float getVidaMax() {
		return vidaMax;
	}
	public void setVidaMax(Float vidaMax) {
		this.vidaMax = vidaMax;
	}
	public Float getManaMax() {
		return manaMax;
	}
	public void setManaMax(Float manaMax) {
		this.manaMax = manaMax;
	}
	public Float getMana() {
		return mana;
	}
	public void setMana(Float mana) {
		this.mana = mana;
	}
	public Float getVelociadeMax() {
		return velociadeMax;
	}
	public void setVelociadeMax(Float velociadeMax) {
		this.velociadeMax = velociadeMax;
	}
	public Float getVelocidade() {
		return velocidade;
	}
	public void setVelocidade(Float velocidade) {
		this.velocidade = velocidade;
	}
	
	

}

